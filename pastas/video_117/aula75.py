def double(number: float):
    return 2*number


def triple(number: float):
    return 3*number


def quadruplicate(number: float):
    return 4*number


first_number = double(5.3)
second_number = triple(9.0)
third_number = quadruplicate(2.1)
forth_number = double(3)

print(f'{first_number}')
print(f'{second_number}')
print(f'{third_number}')
print(f'{forth_number}')

